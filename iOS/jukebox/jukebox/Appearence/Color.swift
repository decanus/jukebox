import UIKit

extension UIColor {
    
    class func lightPurpleColor() -> UIColor {
        return UIColor(red: 213 / 255, green: 94 / 255, blue: 163 / 255, alpha: 1)
    }
        
    class func lighterGray() -> UIColor {
        return UIColor(red: 220 / 255, green: 220 / 255, blue: 220 / 255, alpha: 1)
    }
}